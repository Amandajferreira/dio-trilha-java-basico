public class OperadoresUnitarios {
    public static void main(String[] args) {
        int numero = 5;

        numero = - numero;
// numero fica negativo = retorna -5
        System.out.println(numero);

// para tornar-lo positivo, será necessario a multiplicação * -  -1 subtração = +1
           numero = numero * -1;
        System.out.println(numero);
      
    }
}

